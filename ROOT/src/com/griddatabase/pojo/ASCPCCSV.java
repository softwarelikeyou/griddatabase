package com.griddatabase.pojo;

public class ASCPCCSV {
	public static String[] columns = { "deliveryDate", "hourEnding", "ancillaryType", "MCPC", "DSTFlag" };

	private String deliveryDate;
	private String hourEnding;	
	private String ancillaryType;	
	private String MCPC;	
	private String DSTFlag;
	
	public String getDeliveryDate() {
		return deliveryDate;
	}
	public void setDeliveryDate(String value) {
		this.deliveryDate = value;
	}
	
	public String getHourEnding() {
		return hourEnding;
	}
	public void setHourEnding(String value) {
		this.hourEnding = value;
	}
	
	public String getAncillaryType() {
		return ancillaryType;
	}
	public void setAncillaryType(String value) {
		this.ancillaryType = value;
	}
	
	public String getMCPC() {
		return MCPC;
	}
	
	public void setMCPC(String value) {
		this.MCPC = value;
	}
	
	public String getDSTFlag() {
		return DSTFlag;
	}
	public void setDSTFlag(String value) {
		this.DSTFlag = value;
	}
}
