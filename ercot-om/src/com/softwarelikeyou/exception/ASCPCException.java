package com.softwarelikeyou.exception;

import java.io.Serializable;

public class ASCPCException extends LocalizedException { 
	private static final long	serialVersionUID	= 1L;

	public ASCPCException() {
		super();
	}
	
	public ASCPCException(String msg) {
		super(msg);
	}
	
	public ASCPCException(String msgKey, Serializable... varargs) {
		super(msgKey, varargs);
	}
	
	public ASCPCException(Throwable t, String msgKey) {
		super(t, msgKey);
	}

	public ASCPCException(Throwable t, String msgKey, Serializable... varargs) {
		super(t, msgKey, varargs);
	}	
	
	public ASCPCException(Throwable t) {
		super(t);
	}	
}
