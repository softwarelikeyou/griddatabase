package com.softwarelikeyou.exception;

import java.io.Serializable;

public class FileException extends LocalizedException { 
	private static final long	serialVersionUID	= 1L;

	public FileException() {
		super();
	}
	
	public FileException(String msg) {
		super(msg);
	}
	
	public FileException(String msgKey, Serializable... varargs) {
		super(msgKey, varargs);
	}
	
	public FileException(Throwable t, String msgKey) {
		super(t, msgKey);
	}

	public FileException(Throwable t, String msgKey, Serializable... varargs) {
		super(t, msgKey, varargs);
	}	
	
	public FileException(Throwable t) {
		super(t);
	}		
	
}
