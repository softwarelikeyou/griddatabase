package com.softwarelikeyou.exception;

import java.io.Serializable;

public class ZipCodeException extends LocalizedException { 
	private static final long	serialVersionUID	= 1L;

	public ZipCodeException() {
		super();
	}
	
	public ZipCodeException(String msg) {
		super(msg);
	}
	
	public ZipCodeException(String msgKey, Serializable... varargs) {
		super(msgKey, varargs);
	}
	
	public ZipCodeException(Throwable t, String msgKey) {
		super(t, msgKey);
	}

	public ZipCodeException(Throwable t, String msgKey, Serializable... varargs) {
		super(t, msgKey, varargs);
	}	
	
	public ZipCodeException(Throwable t) {
		super(t);
	}		
	
}
